1.What is a DBMS?
A Database Management System (DBMS) is software that enables users to create, manage, 
retrieve, and manipulate data efficiently in a structured way. It acts as an interface 
between the user and the database, ensuring data integrity, security, and consistency.

2.Relational DBMS (RDBMS)
Data is stored in tables (relations) with rows and columns.
Uses Structured Query Language (SQL) to manage and query data.
Examples: MySQL, PostgreSQL, Oracle, Microsoft SQL Server.

3.What is MySQL?
MySQL is an open-source relational database management system (RDBMS) that is widely used for 
storing and managing structured data. It is one of the most popular database systems and is commonly 
used in web applications, enterprise software, data warehousing, and logging applications.

MySQL is based on Structured Query Language (SQL) and follows the client-server architecture. 
It was initially developed by MySQL AB in 1995 and later acquired by Sun Microsystems in 2008, 
which was subsequently acquired by Oracle Corporation in 2010.

4.MySQL Query Language (SQL)
MySQL supports SQL commands, categorized into:

Data Definition Language (DDL) – Defines the structure of databases.

CREATE DATABASE, CREATE TABLE
ALTER TABLE, DROP TABLE
Data Manipulation Language (DML) – Manipulates data within tables.

SELECT, INSERT, UPDATE, DELETE
Data Control Language (DCL) – Controls user access.

GRANT, REVOKE
Transaction Control Language (TCL) – Manages transactions.

COMMIT, ROLLBACK, SAVEPOINT